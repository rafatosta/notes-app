import { Note } from "@/lib/notes";
import Head from "next/head";
import { useEffect, useState } from "react";
import { NoteCard } from "../components/NoteCard";

export default function Home() {
  const [notes, setNotes] = useState<Note[]>([]);

  useEffect(() => {
    fetch("/api/notes")
      .then((response) => response.json())
      .then((res) => setNotes(res.data));
  }, []);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <nav className="fixed top-0">navbar</nav>
      <main className="w-full h-screen flex flex-col gap-8 justify-center items-center text-gray-900 dark:text-gray-50">
        <div className="flex flex-col gap-6 w-5/6 lg:w-1/2">
          {notes.map((note) => (
            <NoteCard title={note.title} content={note.content} />
          ))}
        </div>
      </main>
      <footer className="fixed bottom-0">footer</footer>
    </>
  );
}
